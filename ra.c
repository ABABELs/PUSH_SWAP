#include "push_swap.h"

void	ra(t_list *list)
{
	int		i;

	i = 0;
	while (i + 1 < list->sia)
	{
		swap(&list->sa[i], &list->sa[i + 1]);
		i++;
	}
	write(1, "ra\n", 3);
}