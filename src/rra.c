#include "../inc/push_swap.h"

void	rra(t_list *list)
{
	int	i;

	i = *(list->sia - 1);
	while (i > 0)
	{
		swap(&list->sa[i], &list->sa[i - 1]);
		i--;
	}
	write(1, "rra\n", 4);
}
