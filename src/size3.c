#include "../inc/push_swap.h"

void	size3(int *s, t_list *list)
{
	if (list->argc == 2)
	{
		if (s[0] > s[1])
			sa(s);
	}
	else if (s[0] < s[1] && s[0] < s[2] && s[1] < s[2])
		;
	else if (s[0] < s[1] && s[0] < s[2] && s[1] > s[2])
	{
		rra(list);
		sa(s);
	}
	else if (s[0] > s[1] && s[0] < s[2] && s[1] < s[2])
		sa(s);
	else if (s[0] < s[1] && s[0] > s[2] && s[1] > s[2])
		rra(list);
	else if (s[1] < s[2] && s[0] > s[1] && s[0] > s[2])
		ra(list);
	else if (s[1] > s[2] && s[0] > s[1] && s[0] > s[2])
	{
		ra(list);
		sa(s);
	}
}
