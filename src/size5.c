#include "../inc/push_swap.h"

void	size5(int *s, t_list *list)
{
	int	i;

	i = 0;
	if (list->sia == 5)
	{
		if (s[0] < s[1] && s[0] < s[2] && s[0] < s[3] && s[0] < s[4] && i == 0)
		{
			pb(list);
			i++;
		}
		if (s[0] > s[1] && s[1] < s[2] && s[1] < s[3] && s[1] < s[4] && i == 0)
		{
			sa(list->sa);
			pb(list);
			i++;
		}
	}
}
