#include "../inc/push_swap.h"

void	rrb(t_list *list)
{
	int	i;
	int	j;

	i = *(list->sib - 1);
	j = 0;
	while (i - 1 >= 0)
	{
		swap(&list->sb[i], &list->sb[i - 1]);
		i--;
	}
	write(1, "rrb\n", 4);
}
