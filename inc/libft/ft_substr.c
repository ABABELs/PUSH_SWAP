#include "libft.h"

char	*ft_substr(char const *s, int start, int len)
{
	int				i;
	char			*str;

	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		len = 0;
	if (ft_strlen(s) - start < len)
		len = ft_strlen(s) - start;
	str = malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (i < len && s[start])
			str[i++] = s[start++];
	str[i] = '\0';
	return (str);
}
